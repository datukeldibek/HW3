class Student{
    var name: String
    var lastName: String
    var age: Int
    var gpa: Double
    
    init(name: String, lastName: String, age: Int, gpa: Double) {
        self.name = name
        self.lastName = lastName
        self.age = age
        self.gpa = gpa
    }
}
